public class AllOperators01 {
    public static void main (String args[]){

        System.out.println(4&6);
        System.out.println(4|6);
        System.out.println(4^6);
        System.out.println(~4);
        System.out.println(~0);
        System.out.println(5<<2);
        System.out.println(5>>2);
    }
    
}
